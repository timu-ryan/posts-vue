<template>
  <div>
    It works!
    <div>
      <button @click="addLike">like</button>
      <button @click="addDislike">dislike</button>
    </div>
    <div>likes: <strong>{{ likes }}</strong></div>
    <div>dislikes: <strong>{{ dislikes }}</strong></div>
  </div>
</template>

<script>
  export default {
    data() {
      return {
        likes: 0,
        dislikes: 0,
      }
    },
    methods: {
      addLike() {
        this.likes += 1;
      },
      addDislike() {
        this.dislikes += 1;
      },
    }
  }
</script>

